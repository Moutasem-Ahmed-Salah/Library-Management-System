    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
                DefaultTableModel tablemodel2 = (DefaultTableModel) jTable1.getModel();
        tablemodel2.setRowCount(0);
        try{
        Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("Select * from books where deleted=0");

            while (rs.next()) {

                title = rs.getString("title");
                Category = rs.getString("category");
                availability = rs.getString("Availability");
                bookID = rs.getInt("idBooks");
                rn = rs.getInt("RackNumber");
                
                tablemodel2.addRow(new Object[]{title, Category, bookID, rn,availability });

            }
            stmt.close();
            

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void updateRnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateRnActionPerformed
        int row = jTable1.getSelectedRow();
           title = jTable1.getValueAt(row, 0).toString();
         try {
           PreparedStatement ps=con.prepareStatement("Update books set RackNumber=? where title=?");
           ps.setInt(1, Integer.parseInt(RackNumber.getText()));
           ps.setString(2, title);
           ps.executeUpdate();
           jButton4.doClick();
       } catch (SQLException ex) {
           Logger.getLogger(UpdateBooks.class.getName()).log(Level.SEVERE, null, ex);
       }
        
    }//GEN-LAST:event_updateRnActionPerformed

