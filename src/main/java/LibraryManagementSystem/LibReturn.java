    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel tablemodel1 = (DefaultTableModel) jTable1.getModel();
        tablemodel1.setRowCount(0);
        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("select bookrented,returndate from rentals where returned=1 and returned_to_lib=0 and returndate is not null");
            while (rs.next()) {

                String BT = rs.getString("bookrented");
                String date1 = rs.getString("returndate");
                String d1=dateadapter.convertToLocalDate(date1);
                tablemodel1.addRow(new Object[]{BT, d1});
            }
            stmt.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable1.getSelectedRow();
        String Br = jTable1.getValueAt(row, 0).toString();
        String date = jTable1.getValueAt(row, 1).toString();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy");
        LocalDate datee = LocalDate.parse(date, formatter);
        try {

            PreparedStatement ps = con.prepareStatement("Update rentals set returned_to_lib=1 where bookrented=? and rentdate=?");
               PreparedStatement ps1=con.prepareStatement("Update books set Availability='true'  where title=?");
            ps1.setString(1, Br);
            ps.setString(1, Br);
            ps.setDate(2, dateadapter.convertToSqlDate(datee));
            ps.executeUpdate();
            ps1.executeUpdate();
            jButton1.doClick();
        } catch (SQLException ex) {
            Logger.getLogger(LibReturn.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton2ActionPerformed

