    private void DeleteBookbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBookbtnActionPerformed
        int row = booktable.getSelectedRow();

        title = booktable.getValueAt(row, 0).toString();

        try {

            String queryyy = "update books set deleted=1  where title=?";

            PreparedStatement ps = con.prepareStatement(queryyy);

            ps.setString(1, title);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Book is removed succesfully");
        } catch (SQLException ex) {
            Logger.getLogger(RemoveBooks.class.getName()).log(Level.SEVERE, null, ex);
        }
        jButton1.doClick();


    }//GEN-LAST:event_DeleteBookbtnActionPerformed

