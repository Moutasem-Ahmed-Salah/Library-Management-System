    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel tablemodel2 = (DefaultTableModel) booktable.getModel();
        tablemodel2.setRowCount(0);
        try {
            Statement stmt = con.createStatement();

            ResultSet rs = stmt.executeQuery("Select * from books where deleted=0");
            while (rs.next()) {

                title = rs.getString("title");
                Category = rs.getString("category");
                author = rs.getString("author");
                availability = rs.getString("Availability");
                bookID = rs.getInt("idBooks");
                rn = rs.getInt("RackNumber");
                tablemodel2.addRow(new Object[]{title, Category, author, bookID, rn, availability});

            }
            stmt.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

