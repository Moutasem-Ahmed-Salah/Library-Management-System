    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel tablemodel2 = (DefaultTableModel) booktablee.getModel();
        tablemodel2.setRowCount(0);
        try {
            PreparedStatement ps2 = con.prepareStatement("select count(*) from rentals where username=? and returndate is null");
            ps2.setString(1, user);
            ResultSet rs1 = ps2.executeQuery();
            rs1.next();
            noOfBooks = rs1.getInt(1);

            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("Select * from books where Availability='true' and deleted=0");

            while (rs.next()) {

                title = rs.getString("title");
                Category = rs.getString("category");
                author = rs.getString("author");
                bookID = rs.getInt("idBooks");
                rn = rs.getInt("RackNumber");
                tablemodel2.addRow(new Object[]{title, Category, author, bookID, rn, availability});

            }
            stmt.close();

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

