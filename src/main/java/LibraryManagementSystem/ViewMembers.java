    private void refereshbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refereshbtnActionPerformed
        DefaultTableModel tablemodel1 = (DefaultTableModel) userstable.getModel();
        tablemodel1.setRowCount(0);
        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("Select user_id,username,firstname,lastname from users ");
            while (rs.next()) {
                user.u_id = rs.getInt("user_id");
                user.Username_u = rs.getString("username");
                user.FirstName = rs.getString("firstname");
                user.LastName = rs.getString("lastname");
                tablemodel1.addRow(new Object[]{user.u_id, user.Username_u, user.FirstName, user.LastName});
            }
            stmt.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_refereshbtnActionPerformed

