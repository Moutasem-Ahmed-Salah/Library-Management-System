    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = userstable.getSelectedRow();
        user.Username_u = userstable.getValueAt(row, 1).toString();
        try {

            PreparedStatement ps = con.prepareStatement("Update users set deleted=1 where username=?");
            ps.setString(1, user.Username_u);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "User is removed succesfully");

        } catch (SQLException ex) {
            Logger.getLogger(ViewMembers.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton2ActionPerformed

