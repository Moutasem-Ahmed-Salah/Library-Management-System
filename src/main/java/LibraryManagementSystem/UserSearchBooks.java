    private void DisplayBooksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DisplayBooksActionPerformed
            try{
            DBconnect db = new DBconnect();
            Connection con = db.con();
        
            Statement st = con.createStatement();
            String query = "select idBooks,title,category,author,RackNumber,Availability,PublicationDate from books where deleted=0";
            ResultSet rs = st.executeQuery(query);
            ResultSetMetaData rsmd = rs.getMetaData();
            DefaultTableModel model = (DefaultTableModel) SearchBooksList.getModel();
            model.setRowCount(0);
            
            int cols = rsmd.getColumnCount();
            String[] colName = new String[cols];
            for(int i=0; i<cols; i++){
                colName[i] = rsmd.getColumnName(i+1);
            }
            model.setColumnIdentifiers(colName);

            String idBooks, Title, Category, Author,  RackNo, Availability;
            
            while(rs.next()){
                idBooks=rs.getString(1);
                Title=rs.getString(2);
                Category=rs.getString(3);
                Author = rs.getString(4);
                RackNo = rs.getString(5);
                Availability = rs.getString(6);
                pb=rs.getString(7);
                String PB=dateadapter.convertToLocalDate(pb);
                
                String[] row = {idBooks,Title, Category, Author, RackNo, Availability,PB};
                model.addRow(row);
            }
            st.close();
            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_DisplayBooksActionPerformed

