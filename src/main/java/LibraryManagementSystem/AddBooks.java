    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
           bookType= Category.getSelectedItem().toString();
                   String pdate=pbdate.getText();

           DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy");
    LocalDate date = LocalDate.parse(pdate, formatter);
           
           
        Book b1 =F.getbook(bookType);
        b1.SetBookcateg();
        b1.author=author_name.getText();
        b1.bookID=Integer.parseInt(BookID.getText());
        b1.booktitle=title.getText();
        b1.racknumber=Integer.parseInt(RackNumber.getText());
        String btype=b1.categ;
        String bauthor=b1.author;
        String btitle=b1.booktitle;
        int bID=b1.bookID;
        int bRN=b1.racknumber;
        String baval=String.valueOf(b1.avaliability);
        
         try {
            PreparedStatement ps = con.prepareStatement("insert into books (idBooks,title,category,author,RackNumber,Availability,PublicationDate) values(?,?,?,?,?,?,?)");

            // Specifying the values of it's parameter
            ps.setInt(1, bID);
            ps.setString(2,btitle);
            ps.setString(3, btype);
            ps.setString(4,bauthor);
            ps.setInt(5, bRN);
            ps.setString(6, baval);
            ps.setDate(7,dateadapter.convertToSqlDate(date));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Book is added!");
            
            }
         catch (Exception ex) 
         { ex.printStackTrace();}

        
        
    }//GEN-LAST:event_jButton1ActionPerformed

