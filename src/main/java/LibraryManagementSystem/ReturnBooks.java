    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel tablemodel2 = (DefaultTableModel) RentedTable.getModel();
        tablemodel2.setRowCount(0);
        try {

            PreparedStatement stmt = con.prepareStatement("select * from rentals where username=? and returndate is null and returned=0");
            stmt.setString(1, user);
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {

                BookRented = rs.getString("bookrented");
                DateRented = rs.getString("rentdate");
                String date = dateadapter.convertToLocalDate(DateRented);
                tablemodel2.addRow(new Object[]{BookRented, date});

            }
            stmt.close();

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

